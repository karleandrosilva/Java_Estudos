public class pratica3 {
    public static void main(String[] args) {
        // 8 TIPOS PRIMITIVOS

        // 4 usados para inteiros
        // INTEIROS = byte, short, int, long
        byte w = -128; // -128 até 127
        short x = -32768; // -32.768 até 32.767
        int y = -2147000000; // -2.147.0000.000 até 2.17.000.000
        long z = -900000000; // -9.000.000.000... até 9.000.000.000...

        // BOOLEAN (Verdadeiro ou Falso)
        boolean eBrasileiro = true;
        boolean eGringo = false;

        // CHAR (Armazenar somente uma caractere)
        // Armazena somente um caractere
        char letra = 'A';

        // STRING (Não é um tipo primitivo, e sim uma classe)
        // Com S maiúsculo
        String nome = "Karle";

        // DOUBLE E FLOAT (Ponto Flutuante)
        double idade = 22.50; // Número mais preciso. 4.1656...
        float ano = 440.20F; // Números com ponto Simples. 4.165
    }
}
